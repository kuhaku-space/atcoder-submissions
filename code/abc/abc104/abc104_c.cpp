ll solve(ll d, ll g){
	if(g <= 0) return 0;
	if(d == 0) return LINF;

	ll x = solve(d - 1, g);
	ll y = p[d - 1] + solve(d - 1, g - p[d - 1] * d - c[d - 1]);
	ll t = min(p[d - 1] - 1, g / d + !!(g % d));
	ll z = t + solve(d - 1, g - t * d);
	return min({x, y, z});
}

