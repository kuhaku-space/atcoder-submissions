int main(void) {
    int n, m, l;
    cin >> n >> m >> l;
    vector<ll> a(n), b(m);
    cin >> a >> b;
    vector<pair<int, int>> c(l);
    cin >> c;
    set<pair<int, int>> st;
    for (auto [x, y] : c) st.emplace(x - 1, y - 1);

    vector<int> ord_a(n), ord_b(m);
    iota(all(ord_a), 0);
    iota(all(ord_b), 0);
    sort(all(ord_a), [&](int x, int y) {
        return a[x] > a[y];
    });
    sort(all(ord_b), [&](int x, int y) {
        return b[x] > b[y];
    });
    priority_queue<tuple<ll, int, int>> pq;
    pq.emplace(a[ord_a[0]] + b[ord_b[0]], 0, 0);
    set<pair<int, int>> s;
    while (true) {
        auto [v, x, y] = pq.top();
        pq.pop();
        if (!st.count({ord_a[x], ord_b[y]})) {
            co(v);
            break;
        }
        if (x + 1 < n && !s.count({x + 1, y})) {
            pq.emplace(a[ord_a[x + 1]] + b[ord_b[y]], x + 1, y);
            s.emplace(x + 1, y);
        }
        if (y + 1 < m && !s.count({x, y + 1})) {
            pq.emplace(a[ord_a[x]] + b[ord_b[y + 1]], x, y + 1);
            s.emplace(x, y + 1);
        }
    }

    return 0;
}
